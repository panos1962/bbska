να προσθέσει πεδία, να διαγράψει πεδία, ή να τροποποιήσει τα υπάρχοντα πεδία.
Αφού γίνουν οι απαραίτητες τροποιήσεις, ο χρήστης κάνει κλικ στο πλήκτρο
save προκειμένου να αποθηκευτούν οι αλλαγές.
</p>
