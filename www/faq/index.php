στις μονάδες αυτές ειδικό attribute
<span class="typo">_sort_</span class="typo">
με τιμή κάποιο κλειδί ταξινόμησης.
Το κλειδί ταξινόμησης θα εμφανίζεται στο επάνω αριστερό μέρος κάθε πληροφοριακής
μονάδας.
Αν θέλετε το κλειδί ταξινόμησης να είναι κρυφό, μπορείτε να χρισιμοποιήσετε το attribute
<span class="typo">_tros_</span class="typo">.
</p>
