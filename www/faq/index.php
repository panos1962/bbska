και εφόσον ο χρήστης έχει πρόσβαση στον συγκεκριμένο κόμβο, είτε ως δημιουργός,
είτε επειδή ο κόμβος είναι δημόσιος, θα εμφανιστεί στη σελίδα ο κόμβος αυτός.
</p>
</div>

<div class="erotisi">
<h3>Γιατί κάποιες μονάδες έχουν διπλό περίγραμμα;</h3>

<p>
Οι πληροφοριακές μονάδες που έχουν διπλό περίγραμμα είναι «εκρηκτικές», τουτέστιν
αναπτύσσονται αυτόματα σε όλο το βάθος τους όποτε χρησιμοποιηθούν ως αρχικές
μονάδες της σελίδας. Αυτό επιτυγχάνεται με την εισαγωγή της ιδιότητας
<span class="typo">_explode_</span class="typo">
χωρίς περιεχόμενο.
</p>
</div>

<div class="erotisi">
<h3>Πώς μπορώ να «μπλοκάρω» την ανάπτυξη εκρηκτικών μονάδων σε συγκεκριμένες υπομονάδες;</h3>

<p>
Μπορούμε να εντάξουμε την ιδιότητα
<span class="typo">_neutral_</span class="typo">
χωρίς περιεχόμενο σε υπομονάδες εκρηκτικών μονάδων προκειμένου να «μπλοκάρουμε»
την περαιτέρω ανάπτυξη των εκρηκτικών μονάδων στις συγκεκριμένες υπομονάδες.
Οι υπομονάδες αυτές μπορούν, ωστόσο, να είναι οι ίδιες εκρηκτικές,
πράγμα που σημαίνει ότι αν ανοιχτούν ως ριζικές μονάδες θα ανοίξουν εκρηκτικά.
</p>

<p>
Παρεμεφερής είναι η ιδιότητα
<span class="typo">_block_</span class="typo">
(χωρίς περιεχόμενο) σε υπομονάδες εκρηκτικών μονάδων.
Οι μονάδες αυτές παρέχονται από τον server χωρίς υπομονάδες,
εκτός και αν χρησιμοποιούνται ως ριζικές.
Με αυτόν τον τρόπο μπορούμε να κάνουμε «οικονομία» σε πόρους φροντίζοντας
να «μπλοκάρουμε» αυτόνομες μονάδες και υπομονάδες.
</p>
</div>

