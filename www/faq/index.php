<div class="erotisi">
<h3>Με ποιόν τρόπο καθορίζουμε το χρώμα ή τη γραμματοσειρά της πληροφορίας;</h3>

<p>
Η αλλαγή του χρώματος ή της γραμματοσειράς μπορεί να επιτευχθεί εγκυβωτίζοντας το
επίμαχο κείμενο σε διπλές αγκύλες, π.χ. η πληροφορία
<span class="typo">[[*bbska]]</span>
θα εμφανιστεί ως
<span class="attrEntono attrEntonoWarning">bbska</span> (έντονο), η πληροφορία
<span class="typo">[[#bbska]]</span>
θα εμφανιστεί ως
<span class="attrEntono attrEntonoNotice">bbska</span> (σημείωση), η πληροφορία
<span class="typo">[[~bbska]]</span>
θα εμφανιστεί ως
<span class="attrEntono attrEntonoTrivial">bbska</span> (επισήμανση), η πληροφορία
<span class="typo">[[!bbska]]</span>
θα εμφανιστεί ως
<span class="attrEntono attrEntonoError">bbska</span> (υπόδειξη) και η πληροφορία
<span class="typo">[[@bbska]]</span>
θα εμφανιστεί ως
<span class="attrEntono attrEntonoMono">bbska</span> (γραφομηχανή).
</p>
</div>

<div class="erotisi">
