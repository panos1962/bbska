<div class="erotisi">
<h3>Πώς μετακινώ μια πληροφοριακή μονάδα;</h3>

<p>
Για να μετακινήσει κανείς μια πληροφοριακή μονάδα από μια θέση σε κάποια
άλλη θέση,
θα πρέπει να κάνει επώνυμη χρήση και να είναι ο δημιουργός της μονάδας.
Θα πρέπει, ακόμη, η σελίδα να βρίσκεται σε update mode, δηλαδή ο χρήστης
θα πρέπει να έχει κάνει κλικ στο εικονίδιο με το σφυρί και το γαλλικό
κλειδί, στο αριστερό μέρος της σελίδας.
</p>

<p>
Εφόσον πληρούνται οι παραπάνω προϋποθέσεις, ο χρήστης μπορεί να κάνει κλικ
στην επικεφαλίδα της πληροφοριακής μονάδας την οποία επιθυμεί να μετακινήσει
και κατόπιν κλικ στο πλήκτρο [Cut],
προκειμένου να αποκόψει τη συγκεκριμένη πληροφοριακή μονάδα από τον τρέχοντα κόμβο.
Κατόπιν, ο χρήστης, θα πρέπει να μεταβεί στον κόμβο αφίξεως και εκεί να κάνει
κλικ στο πλήκτρο [Put]. Αυτό θα έχει ως αποτέλεσμα να μετακινηθεί η πληροφοριακή
μονάδα, ως πρώτη υπομονάδα τον κόμβο προορισμού.
</p>

<p>
Αν ο χρήστης επιθυμεί να μεταφέρει την πληροφοριακή μονάδα σε κάποιο άλλο
σημείο του κόμβου και όχι στην αρχή, τότε θα πρέπει να επιλέξει τον
κόμβο δίπλα στον οποίο επιθυμεί να μετακινήσει &ndash;ως sibling&ndash;
την πληροφοριακή μονάδα και, αφού ανοίξει το σχετικό panel,
να κάνει κλικ στο πλήκτρο [Paste].
</p>
