Κάθε μονάδα πληροφορίας φέρει έναν κωδικό αριθμό και στοιχεία της μορφής key/value.
Οι μονάδες πληροφορίας ονομάζονται και κόμβοι και κάθε κόμβος μπορεί να είναι
δημόσιος ή ιδιωτικός.
</p>
